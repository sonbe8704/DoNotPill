package com.example.donotpill;

public class Other {
    private String id_other;
    private int count;

    public Other() {
    }

    public Other(String id_other, int count) {
        this.id_other = id_other;
        this.count = count;
    }

    public String getId_other() {
        return id_other;
    }

    public void setId_other(String id_other) {
        this.id_other = id_other;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
